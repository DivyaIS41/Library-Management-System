    private void btnissueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnissueActionPerformed
       if(txtid.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "Please enter Book ID and Search again");
                txtid.requestFocus();
        }
        else{
            try{
            pst=c.prepareStatement("UPDATE `library`.`book` SET `status` = 'Issued', issuedate = ?, duedate = ?,studentid =? WHERE (`id` = ?)");
            pst.setString(1, txtisdate.getText());
            pst.setString(2, txtdudate.getText());
            pst.setString(3, txtstid.getText());
            pst.setString(4, txtid.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Book Issued");
            clear();
            } catch (SQLException ex) {
                Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
            }
    }       // TODO add your handling code here:
    }//GEN-LAST:event_btnissueActionPerformed

