            pst=c.prepareStatement("SELECT * FROM library.book where id=?");
            pst.setString(1, txtid.getText());
            rs=pst.executeQuery();
            if(rs.next())
                    txtname.setText(rs.getString("name"));
            else
                JOptionPane.showMessageDialog(this, "Please Enter Valid Book ID");
